    private void btnChuyenTienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChuyenTienActionPerformed
        try {
            String tkn = txtSoTKNhan.getText();
            String tien = txtSoTienChuyen.getText();
            String ttkn = txtTenTKNhan.getText();

            if (tkn.equals("") && tien.equals("") && ttkn.equals("")) {
                JOptionPane.showMessageDialog(this, "Cac truong nay la bat buoc");
            } else {

                if (tien.equals("") && !tkn.equals("") && !ttkn.equals("")) {
                    JOptionPane.showMessageDialog(this, "Vui long nhap so tien");
                } 
//                else if(!tien.equals("") && !tkn.equals("") && !ttkn.equals("")){
//                    JOptionPane.showMessageDialog(this, "Vui long nhap stk nguoi nhan");
//                }
                else {
                    int stc = Integer.valueOf(txtSoTienChuyen.getText());
                    if (stc >= 20000000) {
                        JOptionPane.showMessageDialog(this, "So tien chuyen vuot qua cho phep");
                    } else if (stc <= 0) {
                        JOptionPane.showMessageDialog(this, "Khong the chuyen khoan so tien am");
                    } else if (tkn.equals(khb.SoTaiKhoan.trim())) {
                        JOptionPane.showMessageDialog(this, "Khong the chuyen khoan cho chinh chu");
                    } else {
                        if (se.KTSoDu(khb.SoTaiKhoan, stc)) {
                            if (se.ChuyenKhoan(khb.SoTaiKhoan, tkn, stc)) {
                                se.TruTien(khb.SoTaiKhoan, stc);
                                se.CongTien(tkn, stc);
                                int SoDu = Integer.valueOf(txtSoDuTK.getText());
                                int con = SoDu - stc;
                                txtSoDuTK.setText(String.valueOf(con));
                                JOptionPane.showMessageDialog(this, "Chuyen khoan thanh cong");
                            } else {
                                JOptionPane.showMessageDialog(this, "Loi chuyen khoan");
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "So du khong du de chuyen");
                        }
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnChuyenTienActionPerformed

