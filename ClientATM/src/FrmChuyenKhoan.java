    private void txtSoTKNhanFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSoTKNhanFocusLost
        String tkn = txtSoTKNhan.getText();
        try {
            khachhangBean khk = new khachhangBean();
            khk = se.Kttk(tkn);
            if (txtSoTKNhan.equals("")) {
                JOptionPane.showMessageDialog(this, "Nhap so tai khoan nguoi nhan");
            } else if (khk.SoTaiKhoan == null) {
                JOptionPane.showMessageDialog(this, "Ten tai khoan nhan khong ton tai");
            } else {
                txtTenTKNhan.setText(khk.HoTen);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(FrmChuyenKhoan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSoTKNhanFocusLost

